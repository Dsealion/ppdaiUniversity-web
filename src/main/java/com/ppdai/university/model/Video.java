package com.ppdai.university.model;

import java.util.Date;

/**
 * Created by zhangshibo on 2017/8/31.
 */
public class Video {

    private Integer id;
    private String name;
    private String intro;
    private Integer categoryId;
    private Integer videoStateId;
    private String url;
    private String thumbnailUrl;
    private String remark;
    private Date editTime;


    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIntro() {
        return intro;
    }

    public void setIntro(String intro) {
        this.intro = intro;
    }

    public Integer getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Integer categoryId) {
        this.categoryId = categoryId;
    }

    public Integer getVideoStateId() {
        return videoStateId;
    }

    public void setVideoStateId(Integer videoStateId) {
        this.videoStateId = videoStateId;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getThumbnailUrl() {
        return thumbnailUrl;
    }

    public void setThumbnailUrl(String thumbnailUrl) {
        this.thumbnailUrl = thumbnailUrl;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public Date getEditTime() {
        return editTime;
    }

    public void setEditTime(Date editTime) {
        this.editTime = editTime;
    }
}
